<template><div><h1 id="double-quote-token" tabindex="-1"><a class="header-anchor" href="#double-quote-token" aria-hidden="true">#</a> Double Quote (<code v-pre>&quot;</code>) Token</h1>
<blockquote>
<p>Initiates or terminates a string (variables expanded)</p>
</blockquote>
<h2 id="description" tabindex="-1"><a class="header-anchor" href="#description" aria-hidden="true">#</a> Description</h2>
<p>Double quote is used to initiate and terminate strict strings where variables
can be expanded.</p>
<p>Commands can also be quoted using double quotes (eg where a command might
contain a space character in it's name) however variables cannot be used as
part of a command name.</p>
<h2 id="examples" tabindex="-1"><a class="header-anchor" href="#examples" aria-hidden="true">#</a> Examples</h2>
<pre><code>» set: example=&quot;World!&quot;

» out: &quot;Hello $example&quot;
Hello World!
</code></pre>
<h2 id="detail" tabindex="-1"><a class="header-anchor" href="#detail" aria-hidden="true">#</a> Detail</h2>
<p>Quotes can also work over multiple lines</p>
<pre><code>» out: &quot;foo
» bar&quot;
foo
bar
</code></pre>
